class BasicPlottingColors(object):

    @property
    def Black(self):
        return "black"

    @property
    def Blue(self):
        return "blue"

    @property
    def Green(self):
        return "green"

    @property
    def Red(self):
        return "red"

    @property
    def Cyan(self):
        return "cyan"

    @property
    def Magenta(self):
        return "magenta"

    @property
    def Yellow(self):
        return "yellow"

    @property
    def White(self):
        return "white"